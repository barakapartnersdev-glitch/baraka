// مصدر البيانات: مذكرة استثمارية (Investment Memorandum) مرفوعة من المستخدم.
//
// التزام بقواعد الحوكمة:
//   - publicVersion = نسخة عامة غير حساسة فقط (تُعرض للزائر).
//   - sourceData    = بيانات المصدر الحساسة (هوية المالك، تواصله، الموقع الدقيق) — للإدارة فقط، لا تُنشر أبداً.
//   - لا تُكتب الهوية ولا الموقع الدقيق في أي حقل من حقول النسخ المنشورة.
//
// آمن لإعادة التشغيل (idempotent): يحذف فقط الفرصة ذات العنوان الداخلي نفسه ثم يعيد إنشاءها.
// التشغيل:  npm run db:seed:constanta   (أو: node prisma/seed-constanta.mjs)
import { PrismaClient } from "@prisma/client";
import bcrypt from "bcryptjs";

const prisma = new PrismaClient();


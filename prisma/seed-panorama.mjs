// فرصة استثمارية: مشروع أراضٍ سكنية بإطلالة بحرية بانورامية — منطقة بافوس، قبرص (الاتحاد الأوروبي).
// مصدر البيانات: ملفات مرفوعة من المستخدم (مخطط التقسيم، سند الملكية، رخصة التقسيم، المنطقة التخطيطية).
//
// التزام بقواعد الحوكمة:
//   - publicVersion = نسخة عامة غير حساسة فقط (تُعرض للزائر).
//   - sourceData    = بيانات المصدر الحساسة (هوية المالك، الموقع الدقيق، أرقام السند/الضريبة) — للإدارة فقط، لا تُنشر أبداً.
//   - لا تُكتب الهوية ولا الموقع الدقيق ولا أي رقم حسّاس في أي حقل من حقول النسخ المنشورة.
//
// آمن لإعادة التشغيل (idempotent): يحذف فقط الفرصة ذات العنوان الداخلي نفسه ثم يعيد إنشاءها.
// التشغيل:  npm run db:seed:panorama   (أو: node prisma/seed-panorama.mjs)
import { PrismaClient } from "@prisma/client";
import bcrypt from "bcryptjs";

const prisma = new PrismaClient();

// عنوان داخلي ثابت (مرجع الإدارة) — يُستخدم لإعادة التشغيل الآمن.
const INTERNAL_TITLE = "[بانوراما هيلز] مشروع أراضٍ سكنية بإطلالة بحرية — قبرص";

// مالك مرتبط بالفرصة (حساب مرجعي للإدارة، غير مخصّص للدخول).
const OWNER_EMAIL = "panorama-hills-listing@baraka.example";

async function main() {
  console.log("جارٍ إضافة فرصة بانوراما هيلز...");

  // مالك مرجعي (حساب مرجعي فقط) — كلمة المرور معطّلة عملياً.
  const passwordHash = await bcrypt.hash("disabled-listing-account", 10);
  const owner = await prisma.user.upsert({
    where: { email: OWNER_EMAIL },
    update: { accountStatus: "ACTIVE", role: "PROJECT_OWNER" },
    create: {
      email: OWNER_EMAIL,
      fullName: "فرص قبرص",
      role: "PROJECT_OWNER",
      accountStatus: "ACTIVE",
      passwordHash,
    },
  });

  // مراجع (الإدارة) إن وُجد.
  const admin = await prisma.user.findFirst({ where: { role: "ADMIN" } });

  // ربط بوجهة «قبرص» إن كانت مزروعة (يُظهر الفرصة ضمن صفحة الدولة المطابقة).
  const cyprusDestination = await prisma.destination.findUnique({
    where: { countryKey: "cyprus" },
  });

  // حذف نسخة سابقة بالعنوان نفسه (cascade يحذف العلاقات) — لإعادة تشغيل آمنة.
  const removed = await prisma.opportunity.deleteMany({
    where: { title: INTERNAL_TITLE },
  });
  if (removed.count) console.log(`حُذفت ${removed.count} نسخة سابقة من الفرصة.`);

  await prisma.opportunity.create({
    data: {
      title: INTERNAL_TITLE,
      sector: "عقارات وتطوير",
      country: "قبرص",
      state: "PUBLISHED",
      ownerId: owner.id,
      reviewerId: admin?.id ?? null,
      destinationId: cyprusDestination?.id ?? null,

      // نطاق الاستثمار: غير معلن في النسخة العامة (يُكشف للمستثمر المعتمد لاحقاً).
      currency: "EUR",

      // ===== بيانات المصدر (سرّية — للإدارة فقط، لا تُنشر) =====
      sourceData: {
        note:
          "بيانات حساسة من ملفات المشروع — للإدارة فقط، لا تُنشر. تُستكمل الحقول أدناه بعد التحقق من المستندات الأصلية.",
        contactRevealed: false,
        // تُستكمل من المستندات الأصلية (سند الملكية، رسالة الضريبة، رخصة التقسيم، وثيقة المنطقة).
        ownerName: "(يُستكمل من سند الملكية — حسّاس)",
        ownerContact: "(يُستكمل — حسّاس)",
        exactLocation:
          "(الموقع الدقيق والإحداثيات — يُستكمل من مخطط التقسيم وسند الملكية — حسّاس، لا يُكشف إلا بقرار إداري)",
        cadastralNote:
          "(رقم/أرقام التسجيل العقاري ومساحة القطع وعددها — يُستكمل من سند الملكية ومخطط A0 — حسّاس)",
        permitNote:
          "رخصة تقسيم أرض إلى قطع بناء (ΑΔΕΙΑ ΟΙΚΟΠΕΔΟΠΟΙΗΣΗΣ) معتمدة؛ رقم الرخصة وتفاصيلها في المستندات — حسّاس.",
        zoneNote:
          "المنطقة التخطيطية (ΖΩΝΗ) محدّدة؛ رمز المنطقة ونِسب البناء/التغطية في المستندات — حسّاس.",
        vatNote: "وضع ضريبي موثّق (رسالة VAT)؛ الأرقام حسّاسة — للإدارة فقط.",
      },

      // ===== النسخة العامة (غير حساسة — تُعرض للزائر) =====
      publicVersion: {
        displayTitle:
          "مشروع أراضٍ سكنية بإطلالة بحرية بانورامية — منطقة بافوس، قبرص",
        summary:
          "مشروع تطوير أراضٍ سكنية على تلّة خضراء مرتفعة في إحدى أجمل المناطق الساحلية بقبرص، بإطلالة بانورامية مفتوحة على البحر المتوسّط. خُطِّط الموقع ليُقسَّم إلى قطع بناء سكنية مُجهَّزة بالخدمات، مع رخصة تقسيم معتمدة وسند ملكية مُسجَّل — يجمع بين موقع متميّز داخل الاتحاد الأوروبي وأساس قانوني واضح ومنخفض المخاطر.",
        highlights: [
          "إطلالة بحرية بانورامية مفتوحة من موقع مرتفع على تلّة خضراء",
          "رخصة تقسيم أرض إلى قطع بناء سكنية — معتمدة",
          "سند ملكية مُسجَّل — أساس قانوني آمن وشفّاف",
          "منطقة تخطيطية محدَّدة تحكم الاستخدام ونِسب البناء",
          "داخل الاتحاد الأوروبي: إطار قانوني مستقر وحقوق ملكية قوية",
          "منطقة ساحلية مرغوبة بطلب دولي مستمر على الأراضي والمنازل",
          "مسارات عائد متعددة: بيع قطع مُجهَّزة • بناء فلل للبيع/التأجير • احتفاظ لنموّ رأسمالي",
        ].join("\n"),
        details:
          "يقع المشروع داخل الاتحاد الأوروبي في منطقة ساحلية بقبرص، على تلّة خضراء مرتفعة بإطلالة بانورامية على البحر والريف المحيط. خُطِّط الموقع ليُقسَّم إلى قطع بناء سكنية مُجهَّزة بالخدمات، صُمِّمت لتلتقط الإطلالة المفتوحة. العناصر التي تُعطّل المشاريع عادةً محسومة مسبقاً: الملكية مُسجَّلة، ورخصة التقسيم معتمدة، والمنطقة التخطيطية محدَّدة. تتيح الفرصة مسارات عائد متعددة بين تطوير وبيع القطع، أو بناء فلل، أو الاحتفاظ لتحقيق نموّ رأسمالي مع تطوّر المنطقة. ملاحظة: المعلومات أولية وتعريفية ولا تشكّل عرضاً أو استشارة قانونية أو مالية؛ تُكشف التفاصيل الكاملة (المساحات والأرقام والموقع الدقيق والشروط) للمستثمر المعتمد وفق سياسة المنصة. حُذفت عمداً الأسماء وأرقام التسجيل وأي بيانات حسّاسة من هذه النسخة.",
        // الغلاف: صورة الموقع الفعلية المقدّمة من المالك (panorama-hills.jpg) — تُعرض في لوحة الفرص وصفحة التفاصيل.
        // المعرض: صور الغلاف + 3 صور إقليمية حرّة الاستخدام من ويكيميديا كومنز (بافوس/بوليس خريسوخوس).
        // كلها محايدة لغوياً ولا تكشف القطعة بالضبط ولا الهوية (وفق سياسة الحوكمة). الإسنادات في public/opportunities/CREDITS.md.
        imageUrl: "/opportunities/panorama-hills.jpg",
        gallery: [
          "/opportunities/panorama-hills.jpg",
          "/opportunities/panorama-hills-2.jpg",
          "/opportunities/panorama-hills-3.jpg",
          "/opportunities/panorama-hills-4.jpg",
        ],
      },

      publishedAt: new Date(),
    },
  });

  console.log("\n✓ أُضيفت فرصة بانوراما هيلز كفرصة منشورة (نسخة عامة غير حساسة).");
  console.log("شاهدها في: /opportunities وفي قسم «فرص مختارة» بالصفحة الرئيسية.");
  if (!cyprusDestination)
    console.log(
      "تنبيه: وجهة «cyprus» غير مزروعة؛ شغّل db:seed:destinations لربط الفرصة بصفحة قبرص."
    );
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
